class Profile {
  const Profile({
    required this.name,
    required this.role,
    required this.shortBio,
    required this.bio,
    required this.location,
    required this.email,
    required this.phone,
    required this.website,
    required this.cvUrl,
    required this.imageUrl,
  });

  final String name;
  final String role;
  final String shortBio;
  final String bio;
  final String location;
  final String email;
  final String phone;
  final String website;
  final String cvUrl;
  final String imageUrl;
}
